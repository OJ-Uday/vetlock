/**
 * @vetlock/assurance metamorphic pair generators (packet §2.2, §5 P3).
 *
 * A metamorphic pair is two source strings that MUST behave identically at runtime.
 * If the analyzer produces different findings on them, the analyzer's decision surface
 * is inconsistent — a completeness gap.
 *
 * Five pair families ship in P3:
 *   • statement-reorder     — swap two independent top-level statements
 *   • alpha-rename          — rename an LHS binding without touching usage
 *   • equivalent-expression — semantically-equivalent expression forms
 *   • whitespace-comments   — inject comments + whitespace into A to make B
 *   • member-access-form    — `obj.foo` vs `obj["foo"]` (dot vs computed)
 *
 * Each generator is pure: same seed → byte-identical pair. The `allPairGenerators`
 * array is the canonical enumeration and drives the invariance test.
 */

