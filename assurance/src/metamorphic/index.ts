import type { MetamorphicPair, PairGenerator } from './types.js';
import { statementReorder } from './statement-reorder.js';
import { alphaRename } from './alpha-rename.js';
import { equivalentExpression } from './equivalent-expression.js';
import { whitespaceComments } from './whitespace-comments.js';
import { memberAccessForm } from './member-access-form.js';

/**
 * Canonical enumeration of pair generators. Order is stable so that
 * `generatePairBattery(seed, N)` cycles through families in a documented sequence.
 */
export const allPairGenerators: readonly PairGenerator[] = [
  statementReorder,
  alphaRename,
  equivalentExpression,
  whitespaceComments,
  memberAccessForm,
];

/**
 * Produce `count` pairs by cycling through `allPairGenerators`. The i-th pair is
 * generated by family (i mod N) with seed derived from `(seed, i)` — enough to give
 * every pair a distinct, reproducible seed while keeping the whole battery
 * deterministic for a caller-provided root seed.
 *
 * Battery ids are stable per (seed, count, index) so a failing pair replays.
 */
export function generatePairBattery(
  seed: number,
  count: number,
): readonly MetamorphicPair[] {
  if (!Number.isInteger(count) || count < 0) {
    throw new Error(`generatePairBattery: count must be a non-negative integer; got ${count}`);
  }
  if (allPairGenerators.length === 0) {
    throw new Error('generatePairBattery: no generators registered');
  }
  const pairs: MetamorphicPair[] = [];
  for (let i = 0; i < count; i++) {
    const gen = allPairGenerators[i % allPairGenerators.length];
    // Derived seed: mix the root seed with the index so every slot in the battery
    // gets a distinct, reproducible seed. Kept as a 32-bit integer to survive any
    // downstream `>>> 0` normalization.
    const derived = (Math.imul(seed | 0, 0x9e3779b1) + Math.imul(i | 0, 0x85ebca6b)) | 0;
    pairs.push(gen.generate(derived));
  }
  return pairs;
}
